nom = str(input("Introduce el nombre: "))

apellidos = str(input("Introduce tu apellido: "))

nom = nom.lower()
apellidos = apellidos.lower()
print ("Tu nombre en minuscula es:", nom, apellidos)

nom = nom.upper()
apellidos = apellidos.upper()
print ("Tu nombre en minuscula es:", nom, apellidos)

nom = nom.title()
apellidos = apellidos.title()
print ("La primera letra de tu nombre y apellido en mayusculas es: ", nom, apellidos)

